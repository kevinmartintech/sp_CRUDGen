                UNION ALL
                SELECT
                    ProcedureType = 'Upsert'
                WHERE
                    @GenerateUpsert = 1
                UNION ALL
                SELECT
                    ProcedureType = 'Indate'
                WHERE
                    @GenerateIndate = 1
                UNION ALL
                SELECT
                    ProcedureType = 'Delete'
                WHERE
                    @GenerateDelete = 1
                UNION ALL
                SELECT
                    ProcedureType = 'DeleteMultiple'
                WHERE
                    @GenerateDeleteMultiple = 1
                UNION ALL
                SELECT
                    ProcedureType = 'Search'
                WHERE
                    @GenerateSearch = 1
            )                                       AS P
