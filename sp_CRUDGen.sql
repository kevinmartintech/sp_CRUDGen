                    @GenerateCreate = 1
                UNION ALL
                SELECT
                    ProcedureType = 'CreateMultiple'
                WHERE
                    @GenerateCreateMultiple = 1
                UNION ALL
                SELECT
                    ProcedureType = 'Read'
                WHERE
                    @GenerateRead = 1
                UNION ALL
                SELECT
                    ProcedureType = 'ReadEager'
                WHERE
                    @GenerateReadEager = 1
                UNION ALL
                SELECT
                    ProcedureType = 'Update'
